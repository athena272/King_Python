n= input("Digita qualquer coisa, por favor: ")
print("Tipo da váriavel digitado")
print(type(n))
print('----------------------------------------------')
print("Ver se o que foi digitado pode ser um número inteiro:")
print(n.isnumeric())
print('----------------------------------------------')
print("Ver se o que foi digitado é somente letras:")
print(n.isalpha())
print('----------------------------------------------')
print("Ver se o que foi digitado é ALFANUMÉRICO:")
print(n.isalnum())
print('----------------------------------------------')
print("Ver se o que foi digitado está somente com letras MAIÚSCULAS:")
print(n.isupper())
print('----------------------------------------------')





